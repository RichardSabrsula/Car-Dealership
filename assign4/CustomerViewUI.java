    /*
    * @param searchDatabaseVIN button pushed
    * Searches database for VIN and outputs appropriate response.
    */
    private void searchDatabaseVINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchDatabaseVINActionPerformed
        //sets cursor to waiting
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        String vin = vinPane.getText();
        final Vehicle v = dealership.getVehInv().searchInventory(vin);
        messagePane.setText("works");
        //if vehicle exists, output it to table, according to type
        if (v != null) {
            //type PassengerCar
            if(v instanceof PassengerCar) {
                PassengerCar car = (PassengerCar) v;
                String make = car.getMake();
                String model = car.getModel();
                int year = car.getYear();
                int mileage = car.getMileage();
                float price = car.getPrice();
                String body = car.getBodyStyle();
                resultsTable.setModel(new javax.swing.table.DefaultTableModel(
                    new Object [][] {
                        {make, model, year, mileage, price, vin, body}
                    },
                    new String [] {
                        "Make", "Model", "Year", "Mileage", "Price", "VIN", "Extra info"
                    }
                    ));
                //resizes and centers window popup with results
                searchResults.setBounds(100, 100, 500, 300);
                searchResults.setLocationRelativeTo(this);
                //Type Truck
            } else if (v instanceof Truck) {
                Truck truck = (Truck) v;
                String make = truck.getMake();
                String model = truck.getModel();
                int year = truck.getYear();
                int mileage = truck.getMileage();
                float price = truck.getPrice();
                float length = truck.getLength();
                float load = truck.getMaxLoadWeight();
                String extra = "Length(ft): " + length + " Max Load(lbs): " + load;
                resultsTable.setModel(new javax.swing.table.DefaultTableModel(
                    new Object [][] {
                        {make, model, year, mileage, price, vin, extra}
                    },
                    new String [] {
                        "Make", "Model", "Year", "Mileage", "Price", "VIN", "Extra info"
                    }
                    ));
                //resizes and centers window popup with results
                resultsTable.getColumn("Extra info").setWidth(350);
                resultsTable.getColumn("Extra info").setPreferredWidth(350);
                searchResults.setBounds(100, 100, 700, 300);
                searchResults.setLocationRelativeTo(this);
                //Type Motorcycle
            } else {
                Motorcycle motor = (Motorcycle) v;
                String make = motor.getMake();
                String model = motor.getModel();
                int year = motor.getYear();
                int mileage = motor.getMileage();
                float price = motor.getPrice();
                float disp = motor.getDisplacement();
                String type = motor.getType();
                String extra = "Displacement: " + disp + " Engine type: " + type;
                resultsTable.setModel(new javax.swing.table.DefaultTableModel(
                    new Object [][] {
                        {make, model, year, mileage, price, vin, extra}
                    },
                    new String [] {
                        "Make", "Model", "Year", "Mileage", "Price", "VIN", "Extra info"
                    }
                    ));
                //resizes and centers windows popup with results
                resultsTable.getColumn("Extra info").setWidth(350);
                resultsTable.getColumn("Extra info").setPreferredWidth(350);
                searchResults.setBounds(100, 100, 800, 300);
                searchResults.setLocationRelativeTo(this);
            }
            //Reverts cursor, shows reults
            this.setCursor(Cursor.getDefaultCursor());
            searchResults.setVisible(true);
            v.print();
        } else {
            //If vehicle doesn't exit, display proper message
            this.setCursor(Cursor.getDefaultCursor());
            noResultsFrame.setBounds(100, 100, 235, 150);
            noResultsFrame.setLocationRelativeTo(this);
            noResultsFrame.setVisible(true);
        }
    }//GEN-LAST:event_searchDatabaseVINActionPerformed

    /* 
    * @param searchDatabaseProp button pushed
    * Searched database for vehicle based on type and properties.
    */
    private void searchDatabasePropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchDatabasePropActionPerformed
        //if Vehicle type is Passengercar
        if(carRadio.isSelected()) {
            //Start waiting cursor
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            String make = makePane.getText();
            String model = modelPane.getText();
            String year = yearPane.getText();
            String priceLow = lowPricePane.getText();
            String priceHigh = highPricePane.getText();
            //search database for matching cases
            try {
                Set<Vehicle> vehicles = dealership.getVehInv().searchInventory(1, make, model, year, priceLow, priceHigh);
                resultsTable.setModel(new javax.swing.table.DefaultTableModel(
                    new Object [vehicles.size()][] ,
                    new String [] {
                        "Make", "Model", "Year", "Mileage", "Price", "VIN", "Extra Info"
                    }
                    ));
                int i = 0;
                //for all matching cases, go through table row by row and add
                for (Vehicle v : vehicles) {
                    PassengerCar car = (PassengerCar) v;
                    resultsTable.setValueAt(v.getMake(), i, 0);
                    resultsTable.setValueAt(v.getModel(), i, 1);
                    resultsTable.setValueAt(v.getYear(), i, 2);
                    resultsTable.setValueAt(v.getMileage(), i, 3);
                    resultsTable.setValueAt(v.getPrice(), i, 4);
                    resultsTable.setValueAt(v.getVin(), i, 5);
                    resultsTable.setValueAt(car.getBodyStyle(), i, 6);
                    i++;
                }
                //no matching cases, show appropriate message
                if(vehicles.isEmpty()) {
                    noResultsFrame.setBounds(100, 100, 235, 150);
                    noResultsFrame.setLocationRelativeTo(this);
                    noResultsFrame.setVisible(true);
                } else {
                    //display results
                    searchResults.setBounds(100, 100, 500, 300);
                    searchResults.setLocationRelativeTo(this);
                    searchResults.setVisible(true);
                }
            } catch (BadInputException ex) {
                Logger.getLogger(CustomerViewUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            //revert cursor
            this.setCursor(Cursor.getDefaultCursor());
            //if Vehicle type is Truck
        } else if(truckRadio.isSelected()) {
            //change cursor to waiting
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            String make = makePane.getText();
            String model = modelPane.getText();
            String year = yearPane.getText();
            String priceLow = lowPricePane.getText();
            String priceHigh = highPricePane.getText();
            //search database for matching cases
            try {
                Set<Vehicle> vehicles = dealership.getVehInv().searchInventory(2, make, model, year, priceLow, priceHigh);
                resultsTable.setModel(new javax.swing.table.DefaultTableModel(
                    new Object [vehicles.size()][] ,
                    new String [] {
                        "Make", "Model", "Year", "Mileage", "Price", "VIN", "Extra Info"
                    }
                    ));
                int i = 0;
                //put matching cases into table row by row
                for (Vehicle v : vehicles) {
                    Truck truck = (Truck) v;
                    resultsTable.setValueAt(v.getMake(), i, 0);
                    resultsTable.setValueAt(v.getModel(), i, 1);
                    resultsTable.setValueAt(v.getYear(), i, 2);
                    resultsTable.setValueAt(v.getMileage(), i, 3);
                    resultsTable.setValueAt(v.getPrice(), i, 4);
                    resultsTable.setValueAt(v.getVin(), i, 5);
                    String extra = "Length (ft): " + truck.getLength() + " Max load(lbs): " + truck.getMaxLoadWeight();
                    resultsTable.setValueAt(extra, i, 6);
                    i++;
                }
                resultsTable.getColumn("Extra Info").setWidth(350);
                resultsTable.getColumn("Extra Info").setPreferredWidth(350);
                //if no matching cases, display appropriate message
                if(vehicles.isEmpty()) {
                    noResultsFrame.setBounds(100, 100, 235, 150);
                    noResultsFrame.setLocationRelativeTo(this);
                    noResultsFrame.setVisible(true);
                //display results
                } else {
                    searchResults.setBounds(100, 100, 700, 300);
                    searchResults.setLocationRelativeTo(this);
                    searchResults.setVisible(true);
                }
            } catch (BadInputException ex) {
                Logger.getLogger(CustomerViewUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            //revert cursor
            this.setCursor(Cursor.getDefaultCursor());
        //Vehicle type Motorcycle
        } else if(motorRadio.isSelected()) {
            //set cursor to waiting
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            String make = makePane.getText();
            String model = modelPane.getText();
            String year = yearPane.getText();
            String priceLow = lowPricePane.getText();
            String priceHigh = highPricePane.getText();
            //try to find matching cases in database
            try {
                Set<Vehicle> vehicles = dealership.getVehInv().searchInventory(3, make, model, year, priceLow, priceHigh);
                resultsTable.setModel(new javax.swing.table.DefaultTableModel(
                    new Object [vehicles.size()][] ,
                    new String [] {
                        "Make", "Model", "Year", "Mileage", "Price", "VIN", "Extra Info"
                    }
                    ));
                int i = 0;
                //add matching cases to table row by row
                for (Vehicle v : vehicles) {
                    Motorcycle motor = (Motorcycle) v;
                    resultsTable.setValueAt(v.getMake(), i, 0);
                    resultsTable.setValueAt(v.getModel(), i, 1);
                    resultsTable.setValueAt(v.getYear(), i, 2);
                    resultsTable.setValueAt(v.getMileage(), i, 3);
                    resultsTable.setValueAt(v.getPrice(), i, 4);
                    resultsTable.setValueAt(v.getVin(), i, 5);
                    String extra = "Displacement (cc): " + motor.getDisplacement() + " Type: " + motor.getType();
                    resultsTable.setValueAt(extra, i, 6);
                    i++;
                }
                resultsTable.getColumn("Extra Info").setWidth(350);
                resultsTable.getColumn("Extra Info").setPreferredWidth(350);
                //if no matching cases display appropriate message
                if(vehicles.isEmpty()) {
                    noResultsFrame.setBounds(100, 100, 235, 150);
                    noResultsFrame.setLocationRelativeTo(this);
                    noResultsFrame.setVisible(true);
                //display results
                } else {
                    searchResults.setBounds(100, 100, 800, 300);
                    searchResults.setLocationRelativeTo(this);
                    searchResults.setVisible(true);
                }
            } catch (BadInputException ex) {
                Logger.getLogger(CustomerViewUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            //revert cursor
            this.setCursor(Cursor.getDefaultCursor());
        } else {
            //Error message when no radio buttons are pressed
            noRadioPushed.setBounds(100, 100, 400, 150);
            noRadioPushed.setLocationRelativeTo(this);
            noRadioPushed.setVisible(true);
        }

    }//GEN-LAST:event_searchDatabasePropActionPerformed

