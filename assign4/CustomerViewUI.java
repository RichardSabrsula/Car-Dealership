    /*
    * @param the Send button for sending a message is pushed
    * Sends a message to the dealersip if all fields are filled out.
    */
    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
        String name = namePane.getText();
        String phone = phonePane.getText();
        String email = emailPane.getText();
        String message = messagePane.getText();
        //If all fields filled out, sends message.
        if(name.length() != 0 && phone.length() != 0 && email.length() != 0 && message.length() != 0) {
            //start waiting cursor
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            dealership.getComm().sendMessage(name, phone, email, message); 
            //revert cursor
            this.setCursor(Cursor.getDefaultCursor());
            //display success message
            messageSuccess.setBounds(100, 100, 375, 175);
            messageSuccess.setLocationRelativeTo(this);
            messageSuccess.setVisible(true);
        //Field isn't filled out, display appropriate error message
        } else {
            messageFieldEmpty.setBounds(100, 100, 425, 200);
            messageFieldEmpty.setLocationRelativeTo(this);
            messageFieldEmpty.setVisible(true);
        }
    }//GEN-LAST:event_sendButtonActionPerformed

